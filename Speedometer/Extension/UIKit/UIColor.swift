import UIKit

extension UIColor {
    
    @nonobjc class var backgroundGreen: UIColor {
      return UIColor(red: 209/255, green: 245/255, blue: 211/255, alpha: 1.0)
    }

    @nonobjc class var rouge: UIColor {
      return UIColor(red: 238/255, green: 83 / 255.0, blue: 82/255, alpha: 1.0)
    }

}
